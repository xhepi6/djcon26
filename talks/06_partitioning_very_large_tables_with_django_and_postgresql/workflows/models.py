"""
The model that motivates the whole talk: a workflow-steps table that, at
Kraken's scale, grows to billions of rows.

Two things worth noting:

1. The primary key is composite — ``(id, created_at)``. Postgres requires
   the partition key (``created_at``) to be part of every unique
   constraint, including the primary key. A plain ``id`` PK is rejected
   with ``ERROR: unique constraint on partitioned table must include all
   partitioning columns``.

2. ``managed = False``. Django's migration framework can't emit
   ``PARTITION BY RANGE (...)``, so the table and its child partitions
   are created with raw SQL in migrations/0001_initial.py. The model is
   here purely to give the Django ORM a typed view of the table.
"""

from django.db import models


class WorkflowStep(models.Model):
    # Django 5.2 composite PK. Component fields are plain integer/datetime
    # fields — CompositePrimaryKey doesn't combine with BigAutoField, so the
    # sequence is managed database-side (GENERATED BY DEFAULT AS IDENTITY).
    pk = models.CompositePrimaryKey("id", "created_at")
    id = models.BigIntegerField()
    created_at = models.DateTimeField()

    workflow_id = models.BigIntegerField()
    name = models.CharField(max_length=100)
    status = models.CharField(max_length=20)
    payload = models.JSONField(default=dict)
    completed_at = models.DateTimeField(null=True, blank=True)

    class Meta:
        managed = False
        # Keep Django's auto-generated table name so the SQL migration and
        # the ORM agree on what to query.
        db_table = "workflows_workflowstep"

    def __str__(self):
        return f"step#{self.id} {self.name} ({self.status})"
